package timBulchalka.intro;

public class Variable1 {
    public static void main(String[] args) {
        // Variables are a way to store information in our computer.
        // there are different types of data (Data Types) in Java.
        int myFirstNumber = 5;
        System.out.println(myFirstNumber);

        // An expression is a coding construct that evaluates to a single value.
        // the number 5 above is an expression.
        myFirstNumber = 23;
        System.out.println(myFirstNumber);

        int added = myFirstNumber + 20;
        System.out.println(added);
    }
}
